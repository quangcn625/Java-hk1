public class MonHoc {
    private String mamon, tenmon;
    private int sotienchi;

    public MonHoc(String mamon, String tenmon, int sotienchi) {
        this.mamon = mamon;
        this.tenmon = tenmon;
        this.sotienchi = sotienchi;
    }

    public String getMamon() {
        return mamon;
    }

    public String getTenmon() {
        return tenmon;
    }

    public int getSotienchi() {
        return sotienchi;
    }
}
